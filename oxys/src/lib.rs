#[cfg(test)]
mod tests {
    use super::{GENERATED_TOML_HEADER, manifest_to_toml, parse_generated_manifest_toml};

    #[test]
    fn services_macro_builds_services_struct() {
        let services = crate::services! {
            enabled: ["NetworkManager", "sshd"],
            disabled: ["lvm2-monitor", "multipathd"],
        };

        assert_eq!(
            services.enabled,
            vec!["NetworkManager".to_owned(), "sshd".to_owned()]
        );
        assert_eq!(
            services.disabled,
            vec!["lvm2-monitor".to_owned(), "multipathd".to_owned()]
        );
    }

    #[test]
    fn openrc_services_macro_applies_conditional_boot_services() {
        let services = crate::services! {
            openrc: {
                sysinit: [],
                boot: ["modules"],
                boot_if: [(false, "disabled"), (true, "zram-init")],
                default: [],
                nonetwork: [],
                shutdown: [],
            },
        };

        assert_eq!(services.openrc.boot, vec!["modules", "zram-init"]);
    }

    #[test]
    fn openrc_services_macro_applies_deltas_to_recommended_services() {
        let services = crate::services! {
            openrc: {
                default: [+"sshd", -"modemmanager", +"sshd"],
            },
        };

        assert_eq!(
            services.openrc.default,
            ["local", "netmount", "nftables", "NetworkManager", "sshd"]
        );
        assert_eq!(
            services.openrc.sysinit,
            crate::manifest::OpenrcServices::recommended().sysinit
        );
    }

    #[test]
    fn openrc_services_delta_macro_applies_conditional_boot_services() {
        let services = crate::services! {
            openrc: {
                boot_if: [(false, "disabled"), (true, "zram-init")],
                default: [],
            },
        };

        assert_eq!(
            services.openrc.boot.last().map(String::as_str),
            Some("zram-init")
        );
        assert!(!services.openrc.contains("disabled"));
    }

    #[test]
    fn manifest_writer_inlines_disk_subvolumes() {
        // Subvolumes only belong to (and are only serialized for) a btrfs
        // layout; this exercises the inline-array formatting for that case.
        let manifest = crate::SystemManifest {
            disk: crate::Disk {
                layout: crate::manifest::DiskLayout::Btrfs,
                ..crate::Disk::default()
            },
            ..crate::SystemManifest::default()
        };

        let toml = manifest_to_toml(&manifest).expect("manifest should serialize");

        assert!(toml.contains(
            "subvolumes = [{ name = \"@\", mount = \"/\" }, { name = \"@home\", mount = \"/home\" }, { name = \"@snapshots\", mount = \"/.snapshots\" }, { name = \"@log\", mount = \"/var/log\" }, { name = \"@pkg\", mount = \"/var/cache/portage\" }]"
        ));
        assert!(!toml.contains("[[disk.subvolumes]]"));
        assert!(toml.starts_with(GENERATED_TOML_HEADER));
        assert!(toml.contains("oxys_checksum_sha256 = \""));
    }

    #[test]
    fn ext4_manifest_omits_zfs_and_subvolumes() {
        let manifest = crate::SystemManifest {
            disk: crate::Disk {
                layout: crate::manifest::DiskLayout::Ext4,
                ..crate::Disk::default()
            },
            ..crate::SystemManifest::default()
        };

        let toml = manifest_to_toml(&manifest).expect("manifest should serialize");

        assert!(toml.contains("[disk.ext4]"));
        assert!(!toml.contains("[disk.zfs]"));
        assert!(!toml.contains("disk.zfs.datasets"));
        assert!(!toml.contains("subvolumes ="));
    }

    #[test]
    fn generated_manifest_round_trips_through_checksum_verification() {
        let manifest = crate::SystemManifest::default();
        let toml = manifest_to_toml(&manifest).expect("manifest should serialize");

        // parse verifies the checksum; regenerating from the parsed manifest is
        // a fixed point. We assert TOML idempotency rather than in-memory struct
        // equality because `Disk::default()` carries btrfs subvolumes even in
        // ext4 mode, and those are intentionally not serialized for ext4.
        let parsed = parse_generated_manifest_toml(&toml).expect("manifest should verify");
        let reserialized = manifest_to_toml(&parsed).expect("parsed manifest should serialize");

        assert_eq!(reserialized, toml);
    }

    #[test]
    fn generated_manifest_rejects_manual_edits() {
        let manifest = crate::SystemManifest::default();
        let toml = manifest_to_toml(&manifest).expect("manifest should serialize");
        let edited = toml.replacen("[os]\n", "[os]\nhostname = \"tampered\"\n", 1);

        let error = parse_generated_manifest_toml(&edited).expect_err("checksum must fail");

        assert!(matches!(
            error,
            crate::GeneratedManifestError::ChecksumMismatch
        ));
        assert!(error.to_string().contains("oxys compile"));
        assert!(!error.to_string().contains("oxys build"));
    }
}
