#[test]
fn writes_exact_portage_config_files() -> Result<(), Box<dyn std::error::Error>> {
    let root = test_root("write_portage_config");
    let config_dir = root.join("etc/portage");

    let mut package_use = HashMap::new();
    package_use.insert(
        "gui-wm/niri".to_owned(),
        vec![
            "systemd".to_owned(),
            "dbus".to_owned(),
            "screencast".to_owned(),
            "-debug".to_owned(),
            "-llvm_slot_19".to_owned(),
            "-llvm_slot_18".to_owned(),
            "-llvm_slot_20".to_owned(),
            "dbus".to_owned(),
        ],
    );
    package_use.insert(
        "x11-libs/mesa".to_owned(),
        vec!["wayland".to_owned(), "-X".to_owned()],
    );
    package_use.insert(
        "media-video/pipewire".to_owned(),
        vec!["bluetooth".to_owned(), "-jack".to_owned()],
    );

    let resolution = UseResolution {
        package_use,
        global_use: vec![
            "wayland".to_owned(),
            "-telemetry".to_owned(),
            "systemd".to_owned(),
            "-openrc".to_owned(),
            "-X".to_owned(),
            "-X".to_owned(),
        ],
        accept_keywords: vec![
            "=gui-wm/niri-25.11-r1 ~amd64".to_owned(),
            "=gui-apps/waybar-0.10.3 ~amd64".to_owned(),
            "=gui-wm/niri-25.11-r1 ~amd64".to_owned(),
        ],
        accept_licenses: vec!["GPL-3 gui-wm/niri".to_owned()],
        conflicts: Vec::<Conflict>::new(),
        warnings: Vec::<Warning>::new(),
        decisions: Vec::new(),
    };

    write_portage_config(&resolution, &config_dir)?;

    let jobs = std::thread::available_parallelism()
        .map(|parallelism| parallelism.get())
        .unwrap_or(1);

    let package_use_contents = fs::read_to_string(config_dir.join("package.use"))?;
    let accept_keywords_contents = fs::read_to_string(config_dir.join("package.accept_keywords"))?;
    let accept_licenses_contents = fs::read_to_string(config_dir.join("package.license"))?;
    let make_conf_contents = fs::read_to_string(config_dir.join("make.conf"))?;

    assert_eq!(
        package_use_contents,
        concat!(
            "# generated by oxys - do not edit manually\n",
            "gui-wm/niri -debug -llvm_slot_18 -llvm_slot_19 -llvm_slot_20 dbus screencast systemd\n",
            "media-video/pipewire -jack bluetooth\n",
            "x11-libs/mesa -X wayland\n"
        )
    );
    assert_eq!(
        accept_keywords_contents,
        concat!(
            "# generated by oxys - do not edit manually\n",
            "gui-apps/waybar ~amd64\n",
            "gui-wm/niri ~amd64\n"
        )
    );
    assert_eq!(
        accept_licenses_contents,
        concat!(
            "# generated by oxys - do not edit manually\n",
            "gui-wm/niri GPL-3\n"
        )
    );
    assert_eq!(
        make_conf_contents,
        format!(
            "# generated by oxys - do not edit manually\nUSE=\"-X -openrc -telemetry systemd wayland\"\nMAKEOPTS=\"-j{jobs} -l{jobs}\"\n"
        )
    );

    cleanup(&root)?;
    Ok(())
}

