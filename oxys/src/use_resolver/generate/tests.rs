#[test]
fn plan_writer_emits_directory_backed_policy_files() -> Result<(), Box<dyn std::error::Error>> {
    let root = test_root("plan_writer");
    let config_dir = root.join("etc/portage");
    fs::create_dir_all(config_dir.join("package.use"))?;
    fs::create_dir_all(config_dir.join("package.accept_keywords"))?;
    fs::create_dir_all(config_dir.join("package.license"))?;
    fs::write(
        config_dir.join("package.use/desktop"),
        "existing use policy\n",
    )?;
    fs::write(
        config_dir.join("package.accept_keywords/desktop"),
        "existing keyword policy\n",
    )?;
    fs::write(
        config_dir.join("package.license/firmware"),
        "existing license policy\n",
    )?;
    let mut package_use = HashMap::new();
    package_use.insert("gui-wm/niri".to_owned(), vec!["wayland".to_owned()]);

    let plan = PortagePlan {
        targets: vec!["=gui-wm/niri-1.0.0".to_owned()],
        manifest: Oxys {
            packages: vec![
                Package::new("www-client/firefox"),
                Package::new("gui-wm/niri"),
            ],
            compiler: Compiler {
                optimisation: BuildOptimisation::Balanced,
                ..Compiler::default()
            },
            ..Oxys::default()
        },
        resolution: UseResolution {
            package_use,
            global_use: vec!["wayland".to_owned()],
            accept_keywords: vec!["=gui-wm/niri-1.0.0 ~amd64".to_owned()],
            accept_licenses: vec!["GPL-3 gui-wm/niri".to_owned()],
            conflicts: Vec::new(),
            warnings: Vec::new(),
            decisions: Vec::new(),
        },
        use_binpkgs: false,
    };

    super::write_portage_plan_config(&plan, &config_dir)?;

    assert_eq!(
        fs::read_to_string(config_dir.join("package.use").join("oxys"))?,
        concat!(
            "# generated by oxys - do not edit manually\n",
            "gui-wm/niri wayland\n"
        )
    );
    assert_eq!(
        fs::read_to_string(config_dir.join("package.use").join("pgo"))?,
        concat!(
            "# generated by oxys - do not edit manually\n",
            "www-client/firefox pgo\n"
        )
    );
    assert_eq!(
        fs::read_to_string(config_dir.join("package.accept_keywords/oxys"))?,
        concat!(
            "# generated by oxys - do not edit manually\n",
            "gui-wm/niri ~amd64\n"
        )
    );
    assert_eq!(
        fs::read_to_string(config_dir.join("package.license/oxys"))?,
        concat!(
            "# generated by oxys - do not edit manually\n",
            "gui-wm/niri GPL-3\n"
        )
    );
    assert_eq!(
        fs::read_to_string(config_dir.join("package.use/desktop"))?,
        "existing use policy\n"
    );
    assert_eq!(
        fs::read_to_string(config_dir.join("package.accept_keywords/desktop"))?,
        "existing keyword policy\n"
    );
    assert_eq!(
        fs::read_to_string(config_dir.join("package.license/firmware"))?,
        "existing license policy\n"
    );

    cleanup(&root)?;
    Ok(())
}

fn test_root(name: &str) -> std::path::PathBuf {
    let unique = format!(
        "oxys_portage_generate_{name}_{}_{}",
        std::process::id(),
        chrono::Utc::now().timestamp_nanos_opt().unwrap_or_default()
    );
    std::env::temp_dir().join(unique)
}

fn cleanup(path: &Path) -> Result<(), Box<dyn std::error::Error>> {
    if path.exists() {
        fs::remove_dir_all(path)?;
    }
    Ok(())
}
