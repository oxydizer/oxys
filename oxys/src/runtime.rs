#[derive(Debug, Clone, PartialEq, Eq)]
pub struct RuntimeConfigOutcome {
    pub prime_offload_configured: bool,
    pub prime_primary_configured: bool,
    pub graphics_diagnostics_configured: bool,
    pub swap_configured: bool,
    pub firewall_configured: bool,
}

#[derive(Debug, Error)]
pub enum RuntimeConfigError {
    #[error("failed to create directory {path}: {source}")]
    CreateDir {
        path: PathBuf,
        #[source]
        source: std::io::Error,
    },
    #[error("failed to write runtime config {path}: {source}")]
    WriteFile {
        path: PathBuf,
        #[source]
        source: std::io::Error,
    },
    #[error("failed to set permissions on {path}: {source}")]
    SetPermissions {
        path: PathBuf,
        #[source]
        source: std::io::Error,
    },
    #[error("failed to remove stale runtime config {path}: {source}")]
    RemoveFile {
        path: PathBuf,
        #[source]
        source: std::io::Error,
    },
    #[error("zram is requested but {0} is missing; install sys-block/zram-init")]
    MissingZramInit(PathBuf),
    #[error("zram runtime provisioning requires OpenRC (configured init system: {0:?})")]
    UnsupportedZramInit(InitSystem),
    #[error("failed to create runtime symlink {path}: {source}")]
    CreateSymlink {
        path: PathBuf,
        #[source]
        source: std::io::Error,
    },
    #[error(transparent)]
    InvalidSwap(#[from] SwapResolveError),
    #[error(transparent)]
    InvalidFirewall(#[from] FirewallValidationError),
    #[error("failed to run nft to validate {path}: {source}")]
    FirewallCheckExec {
        path: PathBuf,
        #[source]
        source: std::io::Error,
    },
    #[error("generated nftables ruleset {path} failed `nft -c -f`: {stderr}")]
    FirewallRulesInvalid { path: PathBuf, stderr: String },
}

pub fn sync_runtime_config(
    manifest: &SystemManifest,
    root: &Path,
) -> Result<RuntimeConfigOutcome, RuntimeConfigError> {
    let resolved_swap = manifest.resolved_swap()?;
    if resolved_swap.zram.is_some() && manifest.init_system != InitSystem::Openrc {
        return Err(RuntimeConfigError::UnsupportedZramInit(
            manifest.init_system,
        ));
    }
    let mut outcome = sync_graphics_runtime_config(manifest, root)?;
    sync_swap_config(&resolved_swap, root)?;
    outcome.swap_configured = resolved_swap.zram.is_some() || resolved_swap.disk.is_some();
    outcome.firewall_configured = sync_firewall_config(manifest, root)?;
    Ok(outcome)
}

pub fn sync_graphics_runtime_config(
    manifest: &SystemManifest,
    root: &Path,
) -> Result<RuntimeConfigOutcome, RuntimeConfigError> {
    let mode = prime_mode(&manifest.hardware.graphics);
    let proprietary_modeset = manifest
        .hardware
        .graphics
        .nvidia
        .is_some_and(|nvidia| nvidia.driver == NvidiaDriver::Proprietary && nvidia.modeset);
    let files = [
        GeneratedFile {
            path: root.join(PRIME_ENV_PATH),
            contents: (mode == Some(PrimeMode::Primary)).then_some(render_prime_environment()),
            mode: 0o644,
        },
        GeneratedFile {
            path: root.join(PRIME_PROFILE_PATH),
            contents: (mode == Some(PrimeMode::Primary)).then_some(render_prime_profile()),
            mode: 0o644,
        },
        GeneratedFile {
            path: root.join(PRIME_MODPROBE_PATH),
            contents: proprietary_modeset.then_some(render_prime_modprobe()),
            mode: 0o644,
        },
        GeneratedFile {
            path: root.join(PRIME_RUN_PATH),
            contents: mode.and_then(|mode| match mode {
                PrimeMode::OffloadIntel | PrimeMode::OffloadAmd => Some(render_prime_run(mode)),
                PrimeMode::Primary => None,
            }),
            mode: 0o755,
        },
        GeneratedFile {
            path: root.join(NVIDIA_BLACKLIST_PATH),
            contents: render_nvidia_blacklist(&manifest.hardware.graphics),
            mode: 0o644,
        },
        GeneratedFile {
            path: root.join(GRAPHICS_DIAGNOSTICS_PATH),
            contents: Some(GRAPHICS_DIAGNOSTICS),
            mode: 0o755,
        },
    ];

    for file in files {
        match file.contents {
            Some(contents) => {
                write_generated_file(&file.path, contents, file.mode)?;
            }
            None => remove_if_exists(&file.path)?,
        }
    }
    remove_if_exists(&root.join(LEGACY_PRIME_ENV_PATH))?;
    remove_if_exists(&root.join(LEGACY_PRIME_PROFILE_PATH))?;

    Ok(RuntimeConfigOutcome {
        prime_offload_configured: matches!(
            mode,
            Some(PrimeMode::OffloadIntel | PrimeMode::OffloadAmd)
        ),
        prime_primary_configured: mode == Some(PrimeMode::Primary),
        graphics_diagnostics_configured: true,
        swap_configured: false,
        firewall_configured: false,
    })
}

pub fn sync_swap_config(
    resolved_swap: &ResolvedSwap,
    root: &Path,
) -> Result<(), RuntimeConfigError> {
    write_generated_file(
        &root.join(SWAP_SYSCTL_PATH),
        &format!(
            "# THIS FILE IS AUTO-GENERATED BY OXYS - DO NOT EDIT MANUALLY\nvm.swappiness = {}\n",
            resolved_swap.swappiness
        ),
        0o644,
    )?;

    let zram_conf = root.join(ZRAM_CONF_PATH);
    let Some(zram) = &resolved_swap.zram else {
        remove_if_exists(&zram_conf)?;
        return Ok(());
    };

    let init_script = root.join(ZRAM_INIT_PATH);
    if !init_script.is_file() {
        return Err(RuntimeConfigError::MissingZramInit(init_script));
    }

    let size_mib = zram.size.div_ceil(MB).max(1);
    let config = format!(
        concat!(
            "# THIS FILE IS AUTO-GENERATED BY OXYS - DO NOT EDIT MANUALLY\n",
            "load_on_start=yes\n",
            "unload_on_stop=yes\n",
            "num_devices=1\n",
            "type0=swap\n",
            "size0={}\n",
            "flag0={}\n",
            "algo0={}\n"
        ),
        size_mib,
        zram.priority,
        zram.algorithm.kernel_name()
    );
    write_generated_file(&zram_conf, &config, 0o644)?;

    Ok(())
}

/// Render and stage the manifest's firewall policy under `root`.
///
/// Enabled: validates the manifest (package + OpenRC service must be
/// declared), renders the ruleset deterministically, stages it next to the
/// live file, checks it with `nft -c -f` when nft is available (the ISO build
/// asserts nft exists there; dev machines without it skip the check), and only
/// then renames it into place with mode 0600. The `/etc/conf.d/nftables`
/// policy is pinned to `SAVE_ON_STOP="no"` so an OpenRC shutdown can never
/// overwrite Oxys-managed rules with transient runtime state.
///
/// Disabled: removes both files, but only if Oxys generated them — a
/// hand-managed rules-save is never deleted.
///
/// This never loads rules into the running kernel, so installing to a mounted
/// target cannot disturb the live host's ruleset; `oxys apply` performs the
/// reload separately after this succeeds.
pub fn sync_firewall_config(
    manifest: &SystemManifest,
    root: &Path,
) -> Result<bool, RuntimeConfigError> {
    manifest.validate_firewall()?;
    let rules_path = root.join(NFTABLES_RULES_PATH);
    let conf_path = root.join(NFTABLES_CONF_PATH);
    let Some(rules) = render_nftables_rules(&manifest.firewall) else {
        remove_if_generated_by_oxys(&rules_path)?;
        remove_if_generated_by_oxys(&conf_path)?;
        return Ok(false);
    };

    let staged_path = rules_path.with_extension("oxys-staged");
    write_generated_file(&staged_path, &rules, 0o600)?;
    if let Err(error) = check_nftables_rules(&staged_path) {
        let _ = fs::remove_file(&staged_path);
        return Err(error);
    }
    fs::rename(&staged_path, &rules_path).map_err(|source| RuntimeConfigError::WriteFile {
        path: rules_path.clone(),
        source,
    })?;
    write_generated_file(&conf_path, NFTABLES_CONF_CONTENTS, 0o644)?;
    Ok(true)
}

/// Deterministically render the nftables ruleset for a firewall policy.
/// Returns `None` when the firewall is disabled. The output starts with
/// `flush ruleset`, so loading the file with `nft -f` atomically replaces any
/// previous ruleset.
pub fn render_nftables_rules(firewall: &Firewall) -> Option<String> {
    let Firewall::Nftables {
        incoming,
        forwarding,
        outgoing,
        allow_icmp,
        tcp_ports,
        udp_ports,
    } = firewall
    else {
        return None;
    };

    let mut rules = String::from(concat!(
        "#!/sbin/nft -f\n",
        "# THIS FILE IS AUTO-GENERATED BY OXYS - DO NOT EDIT MANUALLY\n",
        "# Rendered from the Oxys manifest firewall policy.\n",
        "\n",
        "flush ruleset\n",
        "\n",
        "table inet oxys {\n",
    ));
    rules.push_str(&format!(
        "\tchain input {{\n\t\ttype filter hook input priority filter; policy {};\n",
        incoming.keyword()
    ));
    rules.push_str("\t\tiif \"lo\" accept\n");
    rules.push_str("\t\tct state established,related accept\n");
    rules.push_str("\t\tct state invalid drop\n");
    if *allow_icmp {
        rules.push_str("\t\tmeta l4proto { icmp, ipv6-icmp } accept\n");
    }
    // DHCP client replies: broadcast/relayed DHCPv4 offers and DHCPv6
    // advertisements are not always matched by conntrack as replies, so allow
    // them explicitly (server port -> client port).
    rules.push_str("\t\tudp sport 67 udp dport 68 accept\n");
    rules.push_str("\t\tudp sport 547 udp dport 546 accept\n");
    if let Some(ports) = render_port_set(tcp_ports) {
        rules.push_str(&format!("\t\ttcp dport {ports} accept\n"));
    }
    if let Some(ports) = render_port_set(udp_ports) {
        rules.push_str(&format!("\t\tudp dport {ports} accept\n"));
    }
    rules.push_str("\t}\n");
    rules.push_str(&format!(
        "\tchain forward {{\n\t\ttype filter hook forward priority filter; policy {};\n\t}}\n",
        forwarding.keyword()
    ));
    rules.push_str(&format!(
        "\tchain output {{\n\t\ttype filter hook output priority filter; policy {};\n\t}}\n",
        outgoing.keyword()
    ));
    rules.push_str("}\n");
    Some(rules)
}

/// Canonical (sorted, deduplicated) nftables port set, or `None` when empty.
fn render_port_set(ports: &[u16]) -> Option<String> {
    if ports.is_empty() {
        return None;
    }
    let mut ports = ports.to_vec();
    ports.sort_unstable();
    ports.dedup();
    let rendered = ports
        .iter()
        .map(|port| port.to_string())
        .collect::<Vec<_>>()
        .join(", ");
    Some(format!("{{ {rendered} }}"))
}

const NFTABLES_CONF_CONTENTS: &str = concat!(
    "# THIS FILE IS AUTO-GENERATED BY OXYS - DO NOT EDIT MANUALLY\n",
    "# /var/lib/nftables/rules-save is rendered from the Oxys manifest. Never\n",
    "# let `rc-service nftables stop` overwrite it with whatever transient rules\n",
    "# (NetworkManager, containers) happened to be loaded at shutdown.\n",
    "SAVE_ON_STOP=\"no\"\n",
);

/// Check a staged ruleset with `nft -c -f` before it replaces the live file.
///
/// Skipped without root: even in check mode nft opens a netlink socket, which
/// needs CAP_NET_ADMIN, so an unprivileged run (unit tests, dev machines)
/// would fail with EPERM on a perfectly valid ruleset. Every real consumer —
/// the installer, `oxys apply`, and the ISO fsscript assertion — runs as root.
/// A missing nft binary is also not an error: the ISO build asserts nft is
/// present on any image that enables the firewall, and rules are re-validated
/// there.
fn check_nftables_rules(path: &Path) -> Result<(), RuntimeConfigError> {
    if !rustix::process::geteuid().is_root() {
        return Ok(());
    }
    let output = match std::process::Command::new("nft")
        .arg("-c")
        .arg("-f")
        .arg(path)
        .output()
    {
        Ok(output) => output,
        Err(error) if error.kind() == std::io::ErrorKind::NotFound => return Ok(()),
        Err(source) => {
            return Err(RuntimeConfigError::FirewallCheckExec {
                path: path.to_path_buf(),
                source,
            });
        }
    };
    if !output.status.success() {
        return Err(RuntimeConfigError::FirewallRulesInvalid {
            path: path.to_path_buf(),
            stderr: String::from_utf8_lossy(&output.stderr).trim().to_owned(),
        });
    }
    Ok(())
}

/// Remove a file only when Oxys generated it (per its header marker), so a
/// disabled firewall never deletes hand-managed nftables state.
fn remove_if_generated_by_oxys(path: &Path) -> Result<(), RuntimeConfigError> {
    let Ok(contents) = fs::read_to_string(path) else {
        return Ok(());
    };
    if contents
        .lines()
        .take(2)
        .any(|line| line.contains(GENERATED_FILE_MARKER))
    {
        remove_if_exists(path)?;
    }
    Ok(())
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
enum PrimeMode {
    Primary,
    OffloadIntel,
    OffloadAmd,
}

struct GeneratedFile {
    path: PathBuf,
    contents: Option<&'static str>,
    mode: u32,
}

fn prime_mode(graphics: &Graphics) -> Option<PrimeMode> {
    let nvidia = graphics.nvidia?;
    if nvidia.driver != NvidiaDriver::Proprietary {
        return None;
    }
    if nvidia.prime == GraphicsPrimeMode::Primary {
        return Some(PrimeMode::Primary);
    }
    if nvidia.prime != GraphicsPrimeMode::Offload {
        return None;
    }
    match &graphics.mesa.video_cards {
        VideoCards::Explicit(cards) if cards.contains(&VideoCard::Intel) => {
            Some(PrimeMode::OffloadIntel)
        }
        VideoCards::Explicit(cards) if cards.contains(&VideoCard::Amdgpu) => {
            Some(PrimeMode::OffloadAmd)
        }
        _ => None,
    }
}

