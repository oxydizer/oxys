    #[test]
    fn firewall_rules_render_deterministic_locked_down_policy() {
        let firewall = Firewall::Nftables {
            incoming: crate::manifest::FirewallPolicy::Drop,
            forwarding: crate::manifest::FirewallPolicy::Drop,
            outgoing: crate::manifest::FirewallPolicy::Accept,
            allow_icmp: true,
            tcp_ports: vec![80, 22, 80],
            udp_ports: vec![],
        };

        let rules = render_nftables_rules(&firewall).expect("enabled firewall renders");

        assert!(rules.starts_with("#!/sbin/nft -f\n"));
        assert!(rules.contains("flush ruleset"));
        assert!(rules.contains("type filter hook input priority filter; policy drop;"));
        assert!(rules.contains("type filter hook forward priority filter; policy drop;"));
        assert!(rules.contains("type filter hook output priority filter; policy accept;"));
        assert!(rules.contains("iif \"lo\" accept"));
        assert!(rules.contains("ct state established,related accept"));
        assert!(rules.contains("meta l4proto { icmp, ipv6-icmp } accept"));
        assert!(rules.contains("udp sport 67 udp dport 68 accept"));
        assert!(rules.contains("udp sport 547 udp dport 546 accept"));
        // Ports are canonicalised: sorted and deduplicated.
        assert!(rules.contains("tcp dport { 22, 80 } accept"));
        assert!(!rules.contains("udp dport {"));
        assert_eq!(
            render_nftables_rules(&firewall),
            render_nftables_rules(&firewall.clone())
        );
        assert_eq!(render_nftables_rules(&Firewall::Disabled), None);
    }

