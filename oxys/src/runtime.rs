    #[test]
    fn firewall_disable_removes_generated_files_but_keeps_foreign_rules()
    -> Result<(), Box<dyn std::error::Error>> {
        let root = test_root("firewall_transition");
        let enabled = firewalled_manifest(locked_down(vec![]));
        sync_firewall_config(&enabled, &root)?;

        // Enabled -> disabled: our generated files are removed.
        let disabled = SystemManifest::default();
        assert!(!sync_firewall_config(&disabled, &root)?);
        assert!(!root.join(NFTABLES_RULES_PATH).exists());
        assert!(!root.join(NFTABLES_CONF_PATH).exists());

        // A hand-managed rules-save (no Oxys marker) is never deleted.
        let foreign = root.join(NFTABLES_RULES_PATH);
        fs::create_dir_all(foreign.parent().expect("parent"))?;
        fs::write(&foreign, "table inet mine {}\n")?;
        assert!(!sync_firewall_config(&disabled, &root)?);
        assert_eq!(fs::read_to_string(&foreign)?, "table inet mine {}\n");

        cleanup(&root)?;
        Ok(())
    }

    #[test]
    fn firewall_sync_rejects_port_zero_and_missing_package_or_service() {
        let root = test_root("firewall_invalid");

        let zero_port = firewalled_manifest(locked_down(vec![0]));
        assert!(matches!(
            sync_firewall_config(&zero_port, &root).unwrap_err(),
            RuntimeConfigError::InvalidFirewall(
                crate::manifest::FirewallValidationError::ZeroPort { protocol: "tcp" }
            )
        ));

        let mut no_package = firewalled_manifest(locked_down(vec![]));
        no_package.packages.clear();
        assert!(matches!(
            sync_firewall_config(&no_package, &root).unwrap_err(),
            RuntimeConfigError::InvalidFirewall(
                crate::manifest::FirewallValidationError::MissingPackage
            )
        ));

        let mut no_service = firewalled_manifest(locked_down(vec![]));
        no_service.services.openrc.default.clear();
        assert!(matches!(
            sync_firewall_config(&no_service, &root).unwrap_err(),
            RuntimeConfigError::InvalidFirewall(
                crate::manifest::FirewallValidationError::MissingService
            )
        ));

        let mut systemd = firewalled_manifest(locked_down(vec![]));
        systemd.init_system = InitSystem::Systemd;
        assert!(matches!(
            sync_firewall_config(&systemd, &root).unwrap_err(),
            RuntimeConfigError::InvalidFirewall(
                crate::manifest::FirewallValidationError::UnsupportedInit(InitSystem::Systemd)
            )
        ));

        // Nothing was written by any failed sync.
        assert!(!root.join(NFTABLES_RULES_PATH).exists());
        cleanup(&root).unwrap();
    }

